package main.lesson3.Task1;

public class Calculator {
    int first;
    int second;

    int summ(int first, int second) {
        return first + second;
    }

    int substract(int first, int second) { return first - second; }

    int multiply(int first, int second) {
        return first * second;
    }

    int divide(int first, int second) {
        return first / second;
    }
}
