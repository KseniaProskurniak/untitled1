package main.lesson2.Task8;

public class WithBreak {


    public static void main(String[] args) {
        int value = -1;

        switch (value) {
            case 1:
                System.out.println("A");
                break;
            case 2:
                System.out.println("AA");
                break;
            case 3:
                System.out.println("AAA");
                break;
            case 4:
                System.out.println("AAAA");
                break;
            default:
                System.out.println("AAAAA");
        }
    }

}
