package lessons.lesson3.task1;

public class Calculator {

    int sum(int first, int second) {
        return first + second;
    }

    int subtract(int first, int second) {
        return first - second;
    }

    int multiply(int first, int second) {
        return first * second;
    }

    int divide(int first, int second) {
        return first / second;
    }
}

